/**
  ******************************************************************************
  * @file    Unlock.h
  * @brief   开锁/关锁自动流程模块
  *          封装凸轮电机的完整开锁时序：
  *            1. 启动电机 → 检测微动开关到位 → 停止（保持开锁）
  *            2. 延时5秒 → 继续转动 → 延时500ms → 停止（关锁完成）
  *          对外只需调用 Unlock_Start() 启动，主循环调用 Unlock_Tick() 驱动
  ******************************************************************************
  */

#ifndef __UNLOCK_H
#define __UNLOCK_H

#include <stdint.h>

/** 初始化开锁模块，确保电机处于停止状态 */
void Unlock_Init(void);

/** 启动开锁流程（密码验证成功后调用） */
void Unlock_Start(void);

/** 主循环中周期性调用，驱动开锁/保持/关锁状态机 */
void Unlock_Tick(void);

/** 判断是否正在开锁流程中（返回1表示忙，0表示空闲） */
uint8_t Unlock_IsBusy(void);

#endif
