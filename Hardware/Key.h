/**
  ******************************************************************************
  * @file    Key.h
  * @brief   4×3 矩阵键盘驱动对外接口头文件
  *          事件编码规则：
  *            0x00 ~ 0x09 ：数字键0~9
  *            0xFE        ：确认键
  *            0xFD        ：返回键
  *            0xFF        ：无事件
  ******************************************************************************
  */

#ifndef __KEY_H
#define __KEY_H

#include <stdint.h>

/*================== 按键事件编码宏 ==================*/
#define KEY_NONE     0xFF   /**< 无按键事件 */
#define KEY_CONFIRM  0xFE   /**< 确认键事件 */
#define KEY_BACK     0xFD   /**< 返回键事件 */
// 0x00 ~ 0x09 表示数字键0~9

/*================== 对外接口函数声明 ==================*/

void Key_Init(void);        /**< 按键模块初始化（配置GPIO+运行时状态） */
void Key_Scan(void);        /**< 扫描所有按键，检测事件并存入缓冲 */
uint8_t Key_GetEvent(void); /**< 从缓冲中取出一个按键事件，取完即清空 */

#endif
